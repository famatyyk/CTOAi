local function copyList(values)
    local result = {}
    for index, value in ipairs(values or {}) do
        result[index] = value
    end
    return result
end

local function copyTable(value)
    if type(value) ~= "table" then
        return value
    end
    local result = {}
    for key, item in pairs(value) do
        result[key] = copyTable(item)
    end
    return result
end

local function saveDefaults(kind)
    return copyTable(SAVE_DEFAULTS[tostring(kind or "")] or {})
end

local function exportSection(config, sectionName, fields)
    local section = type(config) == "table" and config[sectionName] or nil
    local result = {}
    if type(section) ~= "table" then
        return result
    end
    for _, field in ipairs(fields or {}) do
        result[field] = section[field]
    end
    return result
end

local function profileExportDescriptors()
    local schema = rawget(_G, "CTOA_HELPER_PROFILE_SCHEMA")
    if type(schema) == "table" and type(schema.profileExportDescriptors) == "function" then
        local ok, descriptors = pcall(schema.profileExportDescriptors)
        if ok and type(descriptors) == "table" and #descriptors > 0 then
            return copyTable(descriptors)
        end
    end
    local descriptors = {}
    local order = {"healing", "heal_friend", "conditions", "equipment", "scripting", "tools"}
    for index, sectionName in ipairs(order) do
        descriptors[index] = {
            id = sectionName,
            source = sectionName,
            output = sectionName,
            fields = copyList(FALLBACK_PROFILE_EXPORT_SECTIONS[sectionName] or {}),
            generated = true,
        }
    end
    return descriptors
end

function ProfilePersistence.profileExportDescriptors()
    return profileExportDescriptors()
end

function ProfilePersistence.profilePersistenceValue(functionName, fallback, ...)
    local callback = rawget(ProfilePersistence, tostring(functionName or ""))
    if type(callback) == "function" and
        callback ~= ProfilePersistence.profilePersistenceValue and
        callback ~= ProfilePersistence.profilePersistenceTable then
        local ok, value = pcall(callback, ...)
        if ok and value ~= nil then
            return value
        end
    end
    return fallback
end

function ProfilePersistence.profilePersistenceTable(functionName, fallback, ...)
    local value = ProfilePersistence.profilePersistenceValue(functionName, fallback, ...)
    if type(value) == "table" then
        return value
    end
    return type(fallback) == "table" and fallback or {}
end

function ProfilePersistence.profileCandidates()
    return copyList(PROFILE_CANDIDATES)
end

function ProfilePersistence.uiPrefsCandidates()
