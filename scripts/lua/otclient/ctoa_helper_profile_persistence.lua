end

function ProfilePersistence.saveDefaults(kind)
    return saveDefaults(kind)
end

local function mutableFileName(kind, path)
    local defaults = saveDefaults(kind)
    local file = string.match(tostring(path or ""), "([^/\\]+)$") or tostring(defaults.file_name or "")
    if kind == "ui_prefs" then return "ctoa_user_ui_prefs.lua" end
    if string.sub(file, 1, 10) == "ctoa_user_" then return file end
    local vocation = string.match(file, "ctoa_([a-z][a-z])_profile%.lua$")
    if vocation then
        local prefix = string.gsub(file, "ctoa_" .. vocation .. "_profile%.lua$", "")
        return "ctoa_user_" .. prefix .. vocation .. "_profile.lua"
    end
    return "ctoa_user_ek_profile.lua"
end

function ProfilePersistence.resolveSavePath(kind, explicitPath, workDir)
    local defaults = saveDefaults(kind)
    local explicit = tostring(explicitPath or "")
    local work = tostring(workDir or "")
    if work ~= "" and string.sub(work, -1) ~= "/" and string.sub(work, -1) ~= "\\" then
        work = work .. "/"
    end
    if explicit ~= "" then
        if work ~= "" and not string.match(explicit, "^[A-Za-z]:[/\\]") then
            return work .. mutableFileName(kind, explicit)
        end
        return explicit
    end
    if work ~= "" and type(defaults.work_dir_suffix) == "string" then
        return work .. defaults.work_dir_suffix
    end
    return tostring(defaults.file_name or "")
end

function ProfilePersistence.fallbackSavePath(kind, attemptedPath)
    local defaults = saveDefaults(kind)
    local fallback = tostring(defaults.file_name or "")
    if tostring(attemptedPath or "") ~= fallback then
        return fallback
    end
    return ""
end

function ProfilePersistence.saveText(kind, serialized)
    local defaults = saveDefaults(kind)
    return tostring(defaults.header or "") .. "return " .. tostring(serialized or "nil") .. "\n"
end

function ProfilePersistence.loadSuccessText(kind, pathOrName)
    if kind == "ui_prefs" then
        return "UI prefs loaded: " .. tostring(pathOrName or "")
    end
    return "Profile loaded: " .. tostring(pathOrName or "")
end

function ProfilePersistence.loadFailureText(kind, path, err)
    if kind == "ui_prefs" then
        return "UI prefs load failed: " .. tostring(path or "") .. " " .. tostring(err or "")
    end
    return "Profile load failed: " .. tostring(path or "") .. " " .. tostring(err or "")
end

local function booleanPref(value)
    return value == true or value == "true" or value == 1
end

local function nonEmptyText(value)
    if type(value) == "string" and value ~= "" then
        return value
    end
    return nil
end

function ProfilePersistence.uiPrefsPlan(prefs, config, helpers)
    local data = type(prefs) == "table" and prefs or {}
    local cfg = type(config) == "table" and config or {}
    local helperApi = type(helpers) == "table" and helpers or {}
    local plan = {
        config_updates = {},
        hud_updates = {},
        helper_updates = {},
    }
    local hotkey = nonEmptyText(data.hotkey)
    if hotkey and type(helperApi.normalize_hotkey) == "function" then
        hotkey = helperApi.normalize_hotkey(hotkey)
    end
    if hotkey and hotkey ~= "" then
        plan.config_updates.hotkey = hotkey
    end
    if data.auto_hide_ms ~= nil then
        plan.config_updates.auto_hide_ms = tonumber(data.auto_hide_ms) or cfg.auto_hide_ms
    end
    if data.window_x ~= nil then
        plan.config_updates.window_x = tonumber(data.window_x) or cfg.window_x
    end
    if data.window_y ~= nil then
        plan.config_updates.window_y = tonumber(data.window_y) or cfg.window_y
    end
    if type(data.hud) == "table" then
        local hud = type(cfg.hud) == "table" and cfg.hud or {}
        if data.hud.enabled ~= nil then
            plan.hud_updates.enabled = booleanPref(data.hud.enabled)
        end
        if data.hud.x ~= nil then
            plan.hud_updates.x = tonumber(data.hud.x) or hud.x
        end
        if data.hud.y ~= nil then
            plan.hud_updates.y = tonumber(data.hud.y) or hud.y
        end
    end
    local theme = nonEmptyText(data.theme_preset)
    if theme then
        plan.config_updates.theme_preset = theme
    end
    if data.compact_mode ~= nil then
        plan.config_updates.compact_mode = booleanPref(data.compact_mode)
    end
    for _, key in ipairs({"active_tab", "smoke_tab", "smoke_subtab"}) do
        local value = nonEmptyText(data[key])
        if value then
            plan.helper_updates[key] = value
        end
    end
    return plan
end

function ProfilePersistence.dirtyState(kind, reason)
    local defaults = saveDefaults(kind)
    local state = {
        dirty_status = tostring(defaults.dirty_status or "Autosave: pending"),
        delay_ms = tonumber(defaults.autosave_delay_ms) or 450,
        log_text = "",
    }
    if reason and tostring(reason) ~= "" and type(defaults.dirty_reason_prefix) == "string" then
        state.log_text = defaults.dirty_reason_prefix .. tostring(reason)
    end
    return state
end

function ProfilePersistence.exportProfile(config, profileName)
    local cfg = type(config) == "table" and config or {}
    local tools = type(cfg.tools) == "table" and cfg.tools or {}
    local profile = {
        schema_version = cfg.schema_version or "ctoa-helper-profile-v1",
        vocation = cfg.vocation,
        name = profileName or "Built-in EK",
        enabled = cfg.enabled,
        safe_boot_runtime_disabled = cfg.safe_boot_runtime_disabled,
        tick_ms = cfg.tick_ms,
        modules = copyTable(cfg.modules or {}),
    }
    for _, descriptor in ipairs(profileExportDescriptors()) do
        if descriptor.generated == true then
            profile[descriptor.output] = exportSection(cfg, descriptor.source, descriptor.fields)
        end
    end
    profile.tools.magic_priority = tools.magic_priority or "rotation"
    profile.tools.rotation_preset = tools.rotation_preset or "smart"
    return profile
end

function ProfilePersistence.exportUiPrefs(config, helper)
    local cfg = type(config) == "table" and config or {}
    local ctx = type(helper) == "table" and helper or {}
    local hud = type(cfg.hud) == "table" and cfg.hud or {}
    return {
        hotkey = cfg.hotkey,
        auto_hide_ms = cfg.auto_hide_ms,
        theme_preset = cfg.theme_preset or "classic",
        compact_mode = cfg.compact_mode and true or false,
        window_x = cfg.window_x or 520,
        window_y = cfg.window_y or 34,
        active_tab = ctx.active_tab or "overview",
        hud = {
            enabled = hud.enabled or false,
            x = hud.x or 22,
            y = hud.y or 170,
        },
    }
end

